"""**Ejemplo complejo**"""

# Definiendo dos conjuntos
grupo_a = {"Luis", "Madeleine", "Carlos", "Michelle"}
grupo_b = {"Carlos", "Michelle", "Elena", "Pedro"}

# Unión de conjuntos (todos los elementos únicos)
union = grupo_a | grupo_b
print(union)  # {'Luis', 'Madeleine', 'Carlos', 'Michelle', 'Elena', 'Pedro'}

# Intersección (elementos comunes)
interseccion = grupo_a & grupo_b
print(interseccion)  # {'Carlos', 'Michelle'}

# Diferencia (elementos en A pero no en B)
diferencia = grupo_a - grupo_b
print(diferencia)  # {'Luis', 'Madeleine'}

# Diferencia simétrica (elementos que están en uno u otro, pero no en ambos)
diferencia_simetrica = grupo_a ^ grupo_b
print(diferencia_simetrica)  # {'Luis', 'Madeleine', 'Elena', 'Pedro'}

"""# **Diccionarios (dict)**

Los diccionarios son colecciones desordenadas de pares clave-valor. Permiten acceder a valores mediante una clave en lugar de un índice.

**Ejemplo básico**
"""

# Definiendo un diccionario
persona = {
    "nombre": "Juan",
    "edad": 25,
    "ciudad": "Quito"
}

print(persona)  # {'nombre': 'Juan', 'edad': 25, 'ciudad': 'Quito'}

# Acceder a valores
print(persona["edad"])  # 25

# Modificar valores
persona["edad"] = 30
print(persona)  # {'nombre': 'Juan', 'edad': 30, 'ciudad': 'Quito'}

# Agregar un nuevo par clave-valor
persona["profesion"] = "dentista"
print(persona)  # {'nombre': 'Juan', 'edad': 30, 'ciudad': 'Quito', 'profesion': 'Dentista'}

# Eliminar una clave
del persona["ciudad"]
print(persona)  # {'nombre': 'Juan', 'edad': 30, 'profesion': 'Doctor'}

# Recorrer un diccionario
for clave, valor in persona.items():
    print(f"{clave}: {valor}")

