"""# **Conjuntos (set)**

:Los conjuntos son colecciones no ordenadas y sin elementos duplicados. Son mutables pero sus elementos deben ser inmutables.

**Ejemplo básico**
"""

# num)Definiendo un conjunto
numeros = {6,7,8,9,41,44,44,9}
